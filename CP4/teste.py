def retorna_dois_valores():
    valor1 = 10
    valor2 = 20
    return valor1, valor2

# Chamando a função e armazenando os valores retornados em variáveis separadas
resultado1, resultado2 = retorna_dois_valores()

# Agora você pode usar resultado1 e resultado2 como variáveis separadas
print(resultado1)  # Isso imprimirá 10
print(resultado2)  # Isso imprimirá 20
